package main // Declares the package as 'main', which is required for executable programs in Go.

import "fmt" // Imports the "fmt" package, which provides functions for formatted I/O (like printing to the console).

func main() { // Defines the 'main' function, the entry point for every executable Go program.
	fmt.Println("Hello, World!") // Uses the Println function from the "fmt" package to print the string "Hello, World!" followed by a newline character to the console.
}
